void intensity_to_rgb(uint8_t intensity, uint8_t *r, uint8_t *g, uint8_t *b) {
    if (intensity == 0) {
        *r = 0; *g = 0; *b = 0;
    } else {
        *r = 0;
        *g = 200;
        *b = 0;
    }
}

// Hàm hiển thị spectrum trên ma trận 8x8
void display_spectrum(ws2812_handleTypeDef *ws2812, uint8_t spectrum[MATRIX_WIDTH]) {
    zeroLedValues(ws2812); // Xóa buffer LED

    for (int x = 0; x < MATRIX_WIDTH; x++) {
        uint8_t height = spectrum[x] > MATRIX_HEIGHT ? MATRIX_HEIGHT : spectrum[x];
        for (int y = 0; y < MATRIX_HEIGHT; y++) {
            int led_idx = (x % 2 == 0) ? (x * MATRIX_HEIGHT + y) : (x * MATRIX_HEIGHT + (MATRIX_HEIGHT - 1 - y));
            if (y < height) {
                uint8_t r, g, b;
                intensity_to_rgb(height, &r, &g, &b);
                setLedValues(ws2812, led_idx, r, g, b); // Đặt màu, tự động đặt is_dirty
            }
        }
    }
    // Không cần gọi ws2812_update_buffer, nó sẽ tự động chạy trong callback DMA
}


